package com.eeit144.drinkmaster.dto;

public class MapDto {
	
	private Double lat;
	
	private Double lng;

	public MapDto() {
	}

	public Double getLat() {
		return lat;
	}

	public void setLat(Double lat) {
		this.lat = lat;
	}

	public Double getLng() {
		return lng;
	}

	public void setLng(Double lng) {
		this.lng = lng;
	}

}
